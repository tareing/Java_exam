package day2;

public class Java {
    public static void main(String[] args) {
        int num = 6;
        if(num % 2 == 1){
            System.out.println("even");
        }
        else if(num % 2 == 0){
            System.out.println("odd");
        }
    }
}
//홀수와 짝수를 구별해주세요.
// num이 짝수면 even, 홀수면 odd 출력해주세요.
// 짝수 판별 : 2로 나누어서 떨어지면 짝수